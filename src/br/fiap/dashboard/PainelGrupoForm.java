    public void preencherTabela(int id_grupo){
        DefaultTableModel modelo = (DefaultTableModel) tableMembros.getModel();
        
        DashboardDAO dao = new DashboardDAO();
        
        //String nomeGrupo = lblNomeGrupo.getText();
        
        for(Aluno a:dao.preencherTabela(id_grupo)){
            
            modelo.addRow(new Object[]{
                a.getNome(),
                a.getRm()
            });
            
        }
    }
    
    public void preencherPainel(Grupo grupo){
        String nomeGrupo = grupo.getNome();
        
        lblNomeGrupo.setText(grupo.getNome());
        lblTurma.setText(grupo.getTurma());
        
        preencherTabela(id_grupo);
        preencherFoguete();
    }
    
    public void preencherFoguete(){
        Foguete foguete = new Foguete();
        String nomeGrupo = lblNomeGrupo.getText();
       
        if(dao.checarFoguete(nomeGrupo) == 1){
            panelFoguete.setVisible(true);
            btnCadastrarFoguete.setVisible(false);
            foguete = dao.pesquisarFoguete(nomeGrupo);
            
            //System.out.println(foguete.getNome());
            
            txtNomeFoguete.setText(foguete.getNome());
            txtPesoFoguete.setText(Float.toString(foguete.getPeso()));
        } else {
            panelFoguete.setVisible(false);
        }
    }
    
