    private void btnCadastrarFogueteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarFogueteActionPerformed
        JTextField txtNome = new JTextField();
        JTextField txtPeso = new JTextField();
        String nomeGrupo = lblNomeGrupo.getText();

        Object[] message = {
            "Nome do Foguete", txtNome,
            "Peso do Foguete", txtPeso,

        };
        int option = JOptionPane.showConfirmDialog(this, message, "Preencha os Campos", JOptionPane.OK_CANCEL_OPTION);
        if (option == JOptionPane.OK_OPTION){
            String nome = txtNome.getText();
            float peso = Float.parseFloat(txtPeso.getText());
            
            Foguete foguete = new Foguete(nome, peso, nomeGrupo);
            
        
            if(dao.registrarFoguete(foguete, nomeGrupo)){
                preencherFoguete();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao cadastrar Foguete");
            }
        }
    }//GEN-LAST:event_btnCadastrarFogueteActionPerformed

