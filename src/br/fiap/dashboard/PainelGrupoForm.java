    private void btnAdicionarMembroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarMembroActionPerformed
        JTextField txtNome = new JTextField();
        JTextField txtRM = new JTextField();
        String grupo = lblNomeGrupo.getText();

        Object[] message = {
            "Nome", txtNome,
            "RM", txtRM,

        };
        int option = JOptionPane.showConfirmDialog(this, message, "Preencha os Campos", JOptionPane.OK_CANCEL_OPTION);
        if (option == JOptionPane.OK_OPTION){
            String nome = txtNome.getText();
            int rm = Integer.parseInt(txtRM.getText());
            
            if(txtNome.getText().isEmpty() || txtRM.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Preencha Todos os campos");
            } else {
                Aluno aluno = new Aluno(rm, nome, grupo);
        
                if(dao.registrarAluno(aluno)){
                    preencherTabela(grupo);
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao inserir Aluno");
                }
            }
        }
    }//GEN-LAST:event_btnAdicionarMembroActionPerformed

